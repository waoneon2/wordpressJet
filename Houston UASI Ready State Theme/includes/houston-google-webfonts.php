			'font_heading' => 'oswald',
			'font_body' => 'pt_serif',
			'font_styles' => '300,400,400italic,600,700'
		);
		return $default_fonts;
	}
}

/***** Load Custom Fonts *****/

if (!function_exists('houston_uasi_google_webfonts')) {
	function houston_uasi_google_webfonts() {
		$houston_uasi_fonts = houston_uasi_custom_fonts();
		if ($houston_uasi_fonts['google_webfonts'] == 'enable') {
			global $mh_google_webfonts;
			$font_header_text = '';
			$font_heading = '';
			$font_styles = '';
			$font_subset = '';
			if (!empty($houston_uasi_fonts['font_styles'])) {
				$font_styles = ':' . $houston_uasi_fonts['font_styles'];
			}
			if ($houston_uasi_fonts['font_heading'] != $houston_uasi_fonts['font_body']) {
				$font_heading = '%7c' . $mh_google_webfonts[$houston_uasi_fonts['font_heading']]['location'] . esc_attr($font_styles);
			}
			if ($houston_uasi_fonts['google_webfonts_subsets'] == 'latin_ext') {
				$font_subset = '&amp;subset=latin,latin-ext';
			} elseif ($houston_uasi_fonts['google_webfonts_subsets'] == 'arabic') {
				$font_subset = '&amp;subset=latin,arabic';
			} elseif ($houston_uasi_fonts['google_webfonts_subsets'] == 'cyrillic') {
				$font_subset = '&amp;subset=latin,cyrillic';
			} elseif ($houston_uasi_fonts['google_webfonts_subsets'] == 'cyrillic_ext') {
				$font_subset = '&amp;subset=latin,cyrillic,cyrillic-ext';
			} elseif ($houston_uasi_fonts['google_webfonts_subsets'] == 'greek') {
				$font_subset = '&amp;subset=latin,greek';
			} elseif ($houston_uasi_fonts['google_webfonts_subsets'] == 'greek_ext') {
				$font_subset = '&amp;subset=latin,greek,greek-ext';
			} elseif ($houston_uasi_fonts['google_webfonts_subsets'] == 'hebrew') {
				$font_subset = '&amp;subset=latin,hebrew';
			} elseif ($houston_uasi_fonts['google_webfonts_subsets'] == 'vietnamese') {
				$font_subset = '&amp;subset=latin,vietnamese';
			}
			wp_enqueue_style('houston-google-fonts', 'https://fonts.googleapis.com/css?family=' . $mh_google_webfonts[$houston_uasi_fonts['font_body']]['location'] . esc_attr($font_styles) . $font_heading . $font_header_text . $font_subset, array(), null);
		}
	}
}
add_action('wp_enqueue_scripts', 'houston_uasi_google_webfonts');

/***** Include Typography Custom CSS *****/

if (!function_exists('houston_uasi_fonts_css')) {
	function houston_uasi_fonts_css() {
		$houston_uasi_fonts = houston_uasi_custom_fonts();
		if ($houston_uasi_fonts['google_webfonts'] == 'enable') {
			global $mh_google_webfonts;
			if (!empty($houston_uasi_fonts['font_size']) && $houston_uasi_fonts['font_size'] != 17 || $houston_uasi_fonts['font_heading'] != 'oswald' || $houston_uasi_fonts['font_body'] != 'pt_serif') {
				echo '<style type="text/css">' . "\n";
					if (!empty($houston_uasi_fonts['font_size']) && $houston_uasi_fonts['font_size'] != 17) {
						echo '.entry-content p { font-size: ' . absint($houston_uasi_fonts['font_size']) . 'px; font-size: ' . absint($houston_uasi_fonts['font_size']) / 16 . 'rem; }' . "\n";
					}
					if ($houston_uasi_fonts['font_heading'] != 'oswald') {
						echo '.header-nav, .header-sub, h1, h2, h3, h4, h5, h6, .breadcrumb, .footer-bottom, .widget-list li, .cat-item, .page_item, .menu-item, .button, .commentlist .vcard, .pagination, .houston-share-buttons { font-family: ' . $mh_google_webfonts[$houston_uasi_fonts['font_heading']]['css'] .'; }' . "\n";
					}
					if ($houston_uasi_fonts['font_body'] != 'pt_serif') {
						echo 'body { font-family: ' . $mh_google_webfonts[$houston_uasi_fonts['font_body']]['css'] . '; }' . "\n";
					}
				echo '</style>' . "\n";
			}
		}
	}
}
add_action('wp_head', 'houston_uasi_fonts_css');

