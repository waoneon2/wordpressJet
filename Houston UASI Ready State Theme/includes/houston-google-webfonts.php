/***** Add Custom Typography Options to Customizer *****/

function houston_uasi_typography_options($wp_customize) {
	global $mh_google_webfonts_subsets, $mh_google_webfonts_list;

	/***** Add Section *****/

	$wp_customize->add_section('mh_typo', array('title' => esc_html__('Typography', 'houston-uasi'), 'priority' => 3, 'panel' => 'mh_theme_options'));

    /***** Add Settings *****/

    $wp_customize->add_setting('houston_uasi_options[font_size]', array('default' => 17, 'type' => 'option', 'sanitize_callback' => 'houston_uasi_sanitize_integer'));
	$wp_customize->add_setting('houston_uasi_options[google_webfonts]', array('default' => 'enable', 'type' => 'option', 'sanitize_callback' => 'houston_uasi_sanitize_select'));
	$wp_customize->add_setting('houston_uasi_options[google_webfonts_subsets]', array('default' => 'latin', 'type' => 'option', 'sanitize_callback' => 'mh_sanitize_google_webfonts_subsets'));
	$wp_customize->add_setting('houston_uasi_options[font_heading]', array('default' => 'oswald', 'type' => 'option', 'sanitize_callback' => 'mh_sanitize_google_webfonts'));
	$wp_customize->add_setting('houston_uasi_options[font_body]', array('default' => 'pt_serif', 'type' => 'option', 'sanitize_callback' => 'mh_sanitize_google_webfonts'));
	$wp_customize->add_setting('houston_uasi_options[font_styles]', array('default' => '300,400,400italic,600,700', 'type' => 'option', 'sanitize_callback' => 'houston_uasi_sanitize_text'));

    /***** Add Controls *****/

    $wp_customize->add_control('font_size', array('label' => esc_html__('Change default Font Size (px)', 'houston-uasi'), 'section' => 'mh_typo', 'settings' => 'houston_uasi_options[font_size]', 'priority' => 1, 'type' => 'text'));
    $wp_customize->add_control('google_webfonts', array('label' => esc_html__('Google Webfonts', 'houston-uasi'), 'section' => 'mh_typo', 'settings' => 'houston_uasi_options[google_webfonts]', 'priority' => 2, 'type' => 'select', 'choices' => array('enable' => esc_html__('Enable', 'houston-uasi'), 'disable' => esc_html__('Disable', 'houston-uasi'))));
	$wp_customize->add_control('google_webfonts_subsets', array('label' => esc_html__('Google Webfonts Characters', 'houston-uasi'), 'section' => 'mh_typo', 'settings' => 'houston_uasi_options[google_webfonts_subsets]', 'priority' => 3, 'type' => 'select', 'choices' => $mh_google_webfonts_subsets));
	$wp_customize->add_control('font_heading', array('label' => esc_html__('Google Webfont for Headings', 'houston-uasi'), 'section' => 'mh_typo', 'settings' => 'houston_uasi_options[font_heading]', 'priority' => 4, 'type' => 'select', 'choices' => $mh_google_webfonts_list));
	$wp_customize->add_control('font_body', array('label' => esc_html__('Google Webfont for Body Text', 'houston-uasi'), 'section' => 'mh_typo', 'settings' => 'houston_uasi_options[font_body]', 'priority' => 5, 'type' => 'select', 'choices' => $mh_google_webfonts_list));
	$wp_customize->add_control('font_styles', array('label' => esc_html__('Imported Google Font Styles', 'houston-uasi'), 'section' => 'mh_typo', 'settings' => 'houston_uasi_options[font_styles]', 'priority' => 6, 'type' => 'text'));

}
add_action('customize_register', 'houston_uasi_typography_options');

/***** Data Sanitization *****/

function mh_sanitize_google_webfonts_subsets($input) {
	global $mh_google_webfonts_subsets;
    $valid = $mh_google_webfonts_subsets;
    if (array_key_exists($input, $valid)) {
        return $input;
    } else {
        return '';
    }
}

function mh_sanitize_google_webfonts($input) {
	global $mh_google_webfonts_list;
    $valid = $mh_google_webfonts_list;
    if (array_key_exists($input, $valid)) {
        return $input;
    } else {
        return '';
    }
}

